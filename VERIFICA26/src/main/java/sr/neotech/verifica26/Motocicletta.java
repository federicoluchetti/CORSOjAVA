package sr.neotech.verifica26;

public class Motocicletta {
private String marca;
private String modello;
private Integer livellobenzina;
public String getMarca() {
	return marca;
}
public void setMarca(String marca) {
	this.marca = marca;
}
public String getModello() {
	return modello;
}
public void setModello(String modello) {
	this.modello = modello;
}
public Integer getLivellobenzina() {
	return livellobenzina;
}
public void setLivellobenzina(Integer livellobenzina) {
	this.livellobenzina = livellobenzina;
}
}
